% Set up serial port
%--------------------------------------------------------------------------
% Output: uart_port (serial port to FPGA)
%--------------------------------------------------------------------------
function [uart_port] = setup_serial_port()
%--------------------------------------------------------------------------
% Connect to FPGA
%--------------------------------------------------------------------------
uart_port = serialport('COM5',256000,"Timeout",10);
end
